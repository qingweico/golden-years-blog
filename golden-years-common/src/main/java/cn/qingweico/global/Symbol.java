public interface Symbol {
    // 符号

    /**
     * Dot : "."
     */
    String DOT = ".";

    /**
     * Class : "class"
     */
    String CLASS = "class";

    /**
     * And : "&"
     */
    String AND = "&";

    /**
     * Equal : "="
     */
    String EQUAL = "=";

    /**
     * BACKSLASH: "\"
     */
    String BACKSLASH = "\\";

    /**
     * FULL_STOP: "。"
     */
    String FULL_STOP = "。";

    /**
     * SEMICOLON: ";"
     */
    String SEMICOLON = ";";
    /**
     * POUND_SIGN: "#"; `#`有很多种读法
     */
    String POUND_SIGN = "#";
    /**
     * COLON: ":"
     */
    String COLON = ":";

    /**
     * WHITE_SPACE: " "
     */
    String WHITE_SPACE = " ";
}
